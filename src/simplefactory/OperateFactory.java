package simplefactory;

/**
 * 操作工厂
 */
public class OperateFactory {

    public static Operate createOperate(String operate) {
        Operate oper = null;
        switch (operate) {
            case "+":
                oper = new OperationAdd();
                break;
            case "-":
                oper = new OperationSub();
                break;
            case "*":
                oper = new OperationMul();
                break;
            case "/":
                oper = new OperationDiv();
                break;
        }
        return oper;
    }
}
